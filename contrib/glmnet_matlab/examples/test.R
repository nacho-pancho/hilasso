library(glmnet)
x=rbind(c(1,1),c(2,2),c(3,3))
y=c(1,3,2)
fit1=glmnet(x,y)
print(fit1)
plot(fit1)
g2=c(1,1,2)
g3=c(1,2,3)
fit2=glmnet(x,g2,family="binomial")
fit3=glmnet(x,g3,family="multinomial")
print(fit2)
print(fit3)
predict(fit1,type='coef')
predict(fit1,newx=x[1:2,],s=c(0.01,0.005))
coef(fit1,s=0.01)
predict(fit2,type="response",newx=x[1:2,])
predict(fit2,type="nonzero")
predict(fit3,type="response",newx=x[1:3,],s=0.01)

